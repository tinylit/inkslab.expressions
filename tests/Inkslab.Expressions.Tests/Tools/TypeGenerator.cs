        private static void DefineBinderSkeleton(ScopePlan plan)
        {
            AbstractTypeEmitter t = plan.TypeEmitter;

            // 递归绑定（程序集内可见，仅 __BindMemberRefs 和 __GetMemberRefSource 之间互调）
            MethodEmitter bindRec = t.DefineMethod("__BindMemberRefs",
                MethodAttributes.Assembly | MethodAttributes.Virtual | MethodAttributes.NewSlot |
                MethodAttributes.Final | MethodAttributes.HideBySig,
                typeof(void));
            plan.DeepObjParam = bindRec.DefineParameter(typeof(object[]), "deepObj");
            plan.LevelParam = bindRec.DefineParameter(typeof(int), "level");
            plan.BindRecursiveMethod = bindRec;

            // 获取源值（程序集内可见）
            MethodEmitter getSrc = t.DefineMethod("__GetMemberRefSource",
                MethodAttributes.Assembly | MethodAttributes.Virtual | MethodAttributes.NewSlot |
                MethodAttributes.Final | MethodAttributes.HideBySig,
                typeof(object));
            plan.SlotParam = getSrc.DefineParameter(typeof(int), "slot");
            plan.GetSourceMethod = getSrc;

            // 显式接口实现：通过 DefineMethodOverride 建立 IMemberRefBinder.BindMemberRefs() 的映射。
            // DefineMethodOverride 内部使用 ObtainAttributes → 标记为 Public，但 CLR 同时调用
            // TypeBuilder.DefineMethodOverride 将其绑定到接口，因此可经 IMemberRefBinder 接口调用。
            MethodInfo entryMethodInfo = typeof(IMemberRefBinder).GetMethod(
                nameof(IMemberRefBinder.BindMemberRefs), Type.EmptyTypes)!;
            MethodEmitter bindEntry = t.DefineMethodOverride(ref entryMethodInfo);
            plan.BindEntryMethod = bindEntry;
        }

        private static void EmitBinderBodies(EmitContext ctx)
        {
            int maxDepth = 0;

            foreach (ScopePlan p in ctx.AllPlans)
            {
                if (p.Depth > maxDepth)
                {
                    maxDepth = p.Depth;
                }
            }

            int deepSize = maxDepth + 1;

            foreach (ScopePlan plan in ctx.AllPlans)
            {
                EmitRecursiveBinderBody(plan);
                EmitGetSourceBody(plan);

                if (plan.IsRoot)
                {
                    EmitRootEntryBody(plan, deepSize);
                }
                else
                {
                    EmitNonRootEntryBody(plan);
                }
            }
        }

        private static void EmitNonRootEntryBody(ScopePlan plan)
        {
            // 嵌套类型不应直接调用 BindMemberRefs() 无参重载；必须由根入口分配 deepObj 后传入。
            plan.BindEntryMethod.Append(Expression.Throw(typeof(NotSupportedException),
                "Nested IMemberRefBinder instances must be reached via the root BindMemberRefs() entry."));
        }

        private static void EmitRootEntryBody(ScopePlan rootPlan, int deepSize)
        {
            MethodEmitter entry = rootPlan.BindEntryMethod;

            VariableExpression deepObj = Expression.Variable(typeof(object[]));
            entry.Append(Expression.Assign(deepObj, Expression.NewArray(deepSize, typeof(object))));

            // 经 MethodEmitter 显式实例调用自身递归方法。
            entry.Append(Expression.Call(
                Expression.This(rootPlan.TypeEmitter),
                rootPlan.BindRecursiveMethod,
                deepObj, Expression.Constant(0)));
        }

        private static void EmitRecursiveBinderBody(ScopePlan plan)
        {
            MethodEmitter bind = plan.BindRecursiveMethod;

            // 底层使用常量下标：Inkslab.Expressions 的 ArrayIndex(arr, exprIdx) 在赋值路径上存在
            // "ldc_i4 local" 误发射问题（本应为 ldloc）；本作用域 level 在编译期与 plan.Depth 重合，直接用常量越过该 bug。
            int depth = plan.Depth;

            // deepObj[depth] = (object)this;
            bind.Append(Expression.Assign(
                Expression.ArrayIndex(plan.DeepObjParam, depth),
                Expression.Convert(Expression.This(plan.TypeEmitter), typeof(object))));

            foreach (LocalCrossRef cross in plan.LocalCrossRefs)
            {
                Expression target = Expression.ArrayIndex(plan.DeepObjParam, cross.TargetScope.Depth);
                Expression call = Expression.Call(target, cross.TargetScope.GetSourceMethod,
                    Expression.Constant(cross.TargetSlot));

                Expression value = cross.PropertyType == typeof(object)
                    ? call
                    : Expression.Convert(call, cross.PropertyType);

                bind.Append(Expression.Assign(cross.Field, value));
            }

            foreach (ChildWalk walk in plan.ChildWalks)
            {
                AppendChildWalk(bind, plan, walk);
            }
        }

        private static void EmitGetSourceBody(ScopePlan plan)
        {
            MethodEmitter get = plan.GetSourceMethod;

            for (int i = 0; i < plan.ExportedSources.Count; i++)
            {
                ExportedSource src = plan.ExportedSources[i];

                Expression fieldRead = src.Field;

                Expression boxed = src.FieldType.IsValueType
                    ? Expression.Convert(fieldRead, typeof(object))
                    : Expression.Convert(fieldRead, typeof(object));

                get.Append(Expression.IfThen(
                    Expression.Equal(plan.SlotParam, Expression.Constant(i)),
                    Expression.Return(boxed)));
            }

            // 槽位未匹配时返回 null（理论上不可达，调用方按编译期分配的槽位询问）。
            get.Append(Expression.Return(Expression.Constant(null, typeof(object))));
        }

        private static void AppendChildWalk(MethodEmitter bind, ScopePlan parentPlan, ChildWalk walk)
        {
            // 子作用域下标由 IL 编译期静态决定；传递给受限于接口签名，但接收方使用自身 Depth 常量读写。
            Expression nextLevel = Expression.Constant(parentPlan.Depth + 1);

            if (walk.IsArray)
            {
                VariableExpression arr = Expression.Variable(typeof(object[]));
                bind.Append(Expression.Assign(arr, Expression.Convert(walk.Field, typeof(object[]))));

                BlockExpression notNullBlock = Expression.Block();

                // ForEach(item, source) 由框架自行选择 for/foreach 形态。
                VariableExpression item = Expression.Variable(typeof(object));
                ForEachExpression forEach = Expression.ForEach(item, arr);
                AppendBindOnInstance(forEach, parentPlan.DeepObjParam, nextLevel, item, walk.BindMethodEmitter);

                notNullBlock.Append(forEach);

                bind.Append(Expression.IfThen(
                    Expression.Not(Expression.Call(_referenceEqualsMethod,
                        new Expression[]
                        {
                            Expression.Convert(arr, typeof(object)),
                            Expression.Constant(null, typeof(object))
                        })),
                    notNullBlock));
            }
            else
            {
                VariableExpression c = Expression.Variable(typeof(object));
                bind.Append(Expression.Assign(c, Expression.Convert(walk.Field, typeof(object))));

                BlockExpression notNullBlock = Expression.Block();
                AppendBindOnInstance(notNullBlock, parentPlan.DeepObjParam, nextLevel, c, walk.BindMethodEmitter);

                bind.Append(Expression.IfThen(
                    Expression.Not(Expression.Call(_referenceEqualsMethod,
                        new Expression[]
                        {
                            c,
                            Expression.Constant(null, typeof(object))
                        })),
                    notNullBlock));
            }
        }

        private static void AppendBindOnInstance(BlockExpression block, Expression deepObjExpr, Expression nextLevel, Expression instance, MethodEmitter bindMethod)
        {
            block.Append(Expression.Call(instance, bindMethod, deepObjExpr, nextLevel));
        }

        private static int AllocateOrReuseTargetSlot(ScopePlan targetScope, string targetCode,
            FieldEmitter targetField, Type fieldType)
        {
            for (int i = 0; i < targetScope.ExportedSources.Count; i++)
            {
                if (string.Equals(targetScope.ExportedSources[i].MemberCode, targetCode, StringComparison.Ordinal))
                {
                    return i;
                }
            }

            int slot = targetScope.ExportedSources.Count;

            targetScope.ExportedSources.Add(new ExportedSource
            {
                MemberCode = targetCode,
                Field = targetField,
                FieldType = fieldType
            });

            return slot;
        }
