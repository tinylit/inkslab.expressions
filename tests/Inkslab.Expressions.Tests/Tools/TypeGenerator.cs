        private Type CreateNestedType(AbstractTypeEmitter typeEmitter, MemberInDto parentMember, int level, MemberInDto[] members,
            Dictionary<string, Type> typeCache, ScopePlan currentPlan, EmitContext ctx)
        {
            string prefix = parentMember.Code + ".";
            MemberInDto[] currentLevelMembers = Array.FindAll(members, m => m.Level == level && m.Code.StartsWith(prefix));

            if (currentLevelMembers.Length == 0)
            {
                throw new InvalidOperationException($"No members found at level {level} with prefix {prefix}.");
            }

            if (currentLevelMembers.Length == 1)
            {
                MemberInDto singleMember = currentLevelMembers[0];
                string code = ExtractMemberCode(singleMember.Code, prefix, level);

                if (code == "*")
                {
                    return HandleWildcardMember(typeEmitter, parentMember, singleMember, level, members, typeCache, currentPlan, ctx);
                }
            }

            return parentMember.MemberType == EnumMemberType.Enum
                ? CreateEnumType(currentLevelMembers, parentMember, prefix, level)
                : CreateClassType(typeEmitter, currentLevelMembers, parentMember, prefix, level, members, typeCache, currentPlan, ctx);
        }

        /// <summary>
        ///    创建类型。
        /// </summary>
        /// <param name="namePrefix">名称前缀。</param>
        /// <param name="members">成员。</param>
        /// <param name="baseType">基类型。</param>
        /// <returns>类型。</returns>
        public Type CreateType(string namePrefix, MemberInDto[] members, Type baseType = null)
        {
            ClassEmitter typeEmitter = _emitter.DefineType($"{namePrefix}_{Guid.NewGuid():N}",
                TypeAttributes.Public | TypeAttributes.Class | TypeAttributes.Sealed,
                baseType ?? typeof(object),
                new[] { typeof(IMemberRefBinder) });

            MemberInDto[] currentLevelMembers = Array.FindAll(members, m => m.Level == 0);

            foreach (MemberInDto member in currentLevelMembers)
            {
                if (_strings.Contains(member.Code))
                {
                    throw new InvalidOperationException(
                        $"Member code cannot be one of the reserved keywords: {member.Code} at level 0, full code is {member.Code}.");
                }
            }

            Dictionary<string, Type> typeCache = new(StringComparer.Ordinal);
            EmitContext ctx = new();
            ScopePlan rootPlan = new() { TypeEmitter = typeEmitter, IsRoot = true, Depth = 0 };
            ctx.AllPlans.Add(rootPlan);

            DefineBinderSkeleton(rootPlan);

            AddPropertiesToClass(typeEmitter, currentLevelMembers, string.Empty, 0, members, typeCache, rootPlan, ctx);

            EmitBinderBodies(ctx);

            return typeEmitter.CreateType();
        }

