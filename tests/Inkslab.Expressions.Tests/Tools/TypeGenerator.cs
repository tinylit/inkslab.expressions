        private Type HandleWildcardMember(AbstractTypeEmitter typeEmitter, MemberInDto parentMember, MemberInDto singleMember,
            int level, MemberInDto[] members, Dictionary<string, Type> typeCache, ScopePlan currentPlan, EmitContext ctx)
        {
            if (parentMember.MemberType is EnumMemberType.Enum or EnumMemberType.Object)
            {
                throw new InvalidOperationException(
                    $"Cannot create a type for wildcard member under an {parentMember.MemberType.ToString().ToLower()} type at level {level}, full code is {singleMember.Code}.");
            }

            if (singleMember.MemberType == EnumMemberType.MemberRef)
            {
                throw new InvalidOperationException(
                    $"MemberRef cannot be the single wildcard member at level {level}, full code is {singleMember.Code}.");
            }

            if (singleMember.MemberType is EnumMemberType.Array or EnumMemberType.Object)
            {
                Type type = CreateNestedType(typeEmitter, singleMember, level + 1, members, typeCache, currentPlan, ctx);

                return singleMember.MemberType == EnumMemberType.Array ? type.MakeArrayType() : type;
            }

            return GetTypeFromEnumMemberType(singleMember.MemberType, singleMember.Required);
        }

        private Type CreateEnumType(MemberInDto[] currentLevelMembers, MemberInDto parentMember, string prefix, int level)
        {
            Type underlyingType;

            EnumMemberType enumMemberType;

            try
            {
                enumMemberType = currentLevelMembers.Select(x => x.MemberType).Distinct().Single();
            }
            catch (Exception ex)
            {
                throw new InvalidOperationException(
                    "Multiple member types found at the same level, which is not supported.", ex);
            }

            switch (enumMemberType)
            {
                case EnumMemberType.Byte:
                    underlyingType = typeof(byte);
                    break;
                case EnumMemberType.SByte:
                    underlyingType = typeof(sbyte);
                    break;
                case EnumMemberType.Int16:
                    underlyingType = typeof(short);
                    break;
                case EnumMemberType.UInt16:
                    underlyingType = typeof(ushort);
                    break;
                case EnumMemberType.Int32:
                    underlyingType = typeof(int);
                    break;
                case EnumMemberType.UInt32:
                    underlyingType = typeof(uint);
                    break;
                case EnumMemberType.Int64:
                    underlyingType = typeof(long);
                    break;
                case EnumMemberType.UInt64:
                    underlyingType = typeof(ulong);
                    break;
                default:
                    throw new InvalidOperationException(
                        $"Invalid member type for enum: {enumMemberType} at level {level}, full code is {parentMember.Code}.");
            }

            HashSet<string> hashSet = new();
            HashSet<object> enumValues = new();

            EnumEmitter enumEmitter =
                _emitter.DefineEnum($"Enum_{_regex.Replace(parentMember.Code, "_")}_{Guid.NewGuid():N}",
                    TypeAttributes.Public, underlyingType);

            enumEmitter.DefineCustomAttribute<FlagsAttribute>();

            foreach (MemberInDto member in currentLevelMembers)
            {
                string code = ExtractMemberCode(member.Code, prefix, level);

                ValidateMemberCode(code, member.Code, level, hashSet);

                object enumValue;

                try
                {
                    enumValue = Mapper.Map(member.DefaultValue, underlyingType);
                }
                catch (Exception ex)
                {
                    throw new InvalidOperationException(
                        $"Enum member code is not a valid {underlyingType.Name} value: {member.DefaultValue} at level {level}, full code is {member.Code}.",
                        ex);
                }

                if (enumValues.Add(enumValue))
                {
                    enumEmitter.DefineLiteral(code, enumValue);
                }
                else
                {
                    throw new InvalidOperationException(
                        $"Duplicate enum value detected: {enumValue} for member code: {member.Code} at level {level}.");
                }
            }

            Type enumType = enumEmitter.CreateType();

            return parentMember.Required ? enumType : typeof(Nullable<>).MakeGenericType(enumType);
        }

