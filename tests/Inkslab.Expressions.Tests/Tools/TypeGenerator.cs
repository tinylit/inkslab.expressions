        /// <summary>
        /// 判定 <paramref name="parent"/> 在 <paramref name="childLevel"/> 层是否对应一个"非通配的常规类"成员集合。
        /// 通配（单一 "*" 成员）由 <see cref="HandleWildcardMember"/> 单独处理，不需提前登记祖先字段。
        /// </summary>
        private static bool IsClassNonWildcard(MemberInDto parent, MemberInDto[] allMembers, int childLevel)
        {
            string prefix = parent.Code + ".";
            MemberInDto[] children = Array.FindAll(allMembers,
                m => m.Level == childLevel && m.Code.StartsWith(prefix));

            if (children.Length == 0)
            {
                return false;
            }

            if (children.Length == 1)
            {
                string code = ExtractMemberCode(children[0].Code, prefix, childLevel);

                if (code == "*")
                {
                    return false;
                }
            }

            return true;
        }
