        private void AddPropertiesToClass(AbstractTypeEmitter typeEmitter, MemberInDto[] members, string prefix, int level,
            MemberInDto[] allMembers, Dictionary<string, Type> typeCache,
            ScopePlan currentPlan, EmitContext ctx)
        {
            HashSet<string> hashSet = new();
            // @AI-Generated by Copilot 2026-04-28 17:00:00
            Dictionary<string, MethodEmitter> gettersByCode = new(StringComparer.Ordinal);
            // 同层处理顺序：① 简单类型 ② Object/Array（递归） ③ MemberRef。
            MemberInDto[] orderedMembers = members
                .OrderBy(m => m.MemberType == EnumMemberType.MemberRef ? 2
                    : m.MemberType is EnumMemberType.Object or EnumMemberType.Array ? 1
                    : 0)
                .ToArray();
            // @AI-Generated-End

            Dictionary<MemberInDto, ScopePlan> childPlansByMember = new();

            foreach (MemberInDto member in orderedMembers)
            {
                string code = ExtractMemberCode(member.Code, prefix, level);

                ValidateMemberCode(code, member.Code, level, hashSet);

                Type enumMemberType;
                string memberRefImmediateTarget = null;
                string memberRefLeafTarget = null;
                bool isCrossScopeMemberRef = false;

                if (member.MemberType == EnumMemberType.MemberRef)
                {
                    // @AI-Generated by Copilot 2026-04-28 18:00:00
                    (enumMemberType, memberRefImmediateTarget, memberRefLeafTarget) =
                        ResolveMemberRefType(member, allMembers, typeCache, level);

                    string refParent = GetParentCode(member.Code);
                    string targetParent = GetParentCode(memberRefImmediateTarget);

                    bool sameScope = string.Equals(refParent, targetParent, StringComparison.Ordinal);

                    if (sameScope)
                    {
                        if (!gettersByCode.TryGetValue(memberRefImmediateTarget, out MethodEmitter targetGetter))
                        {
                            throw new InvalidOperationException(
                                $"MemberRef target {memberRefImmediateTarget} getter not yet defined when processing {member.Code}.");
                        }

                        MethodEmitter newGetter = DefineMemberRefDelegationProperty(typeEmitter, member, code,
                            enumMemberType, targetGetter);

                        gettersByCode[member.Code] = newGetter;
                        ctx.GettersByCode[member.Code] = newGetter;
                        ctx.ScopeByMemberCode[member.Code] = currentPlan;
                        ctx.TypesByMemberCode[member.Code] = enumMemberType;
                        typeCache[member.Code] = enumMemberType;

                        continue;
                    }

                    isCrossScopeMemberRef = true;
                    // @AI-Generated-End
                }
                else if (member.MemberType is EnumMemberType.Enum or EnumMemberType.Array or EnumMemberType.Object)
                {
                    // @AI-Generated by Copilot 2026-04-28 20:30:00
                    // Object/Array 的非通配类成员需要提前在 typeCache / FieldsByCode 中登记，
                    // 这样递归处理子成员时，后代里 MemberRef 才能反向引用到祖先（A.B.C → A）。
                    if (member.MemberType is EnumMemberType.Object or EnumMemberType.Array
                        && IsClassNonWildcard(member, allMembers, level + 1))
                    {
                        (NestedClassEmitter shell, ScopePlan childPlan) =
                            CreateClassShell(typeEmitter, member, currentPlan, ctx);

                        Type shellType = shell.UncompiledType;
                        Type ancestorPropType = member.MemberType == EnumMemberType.Array
                            ? shellType.MakeArrayType()
                            : shellType;

                        typeCache[member.Code] = ancestorPropType;

                        (PropertyEmitter _, MethodEmitter ancestorGetter, FieldEmitter ancestorField) =
                            DefineProperty(typeEmitter, member, code, ancestorPropType, null);

                        gettersByCode[member.Code] = ancestorGetter;
                        ctx.GettersByCode[member.Code] = ancestorGetter;
                        ctx.ScopeByMemberCode[member.Code] = currentPlan;
                        ctx.TypesByMemberCode[member.Code] = ancestorPropType;
                        ctx.FieldsByCode[member.Code] = ancestorField;

                        string nestedPrefix = member.Code + ".";
                        MemberInDto[] childLevelMembers = Array.FindAll(allMembers,
                            m => m.Level == level + 1 && m.Code.StartsWith(nestedPrefix));

                        AddPropertiesToClass(shell, childLevelMembers, nestedPrefix, level + 1,
                            allMembers, typeCache, childPlan, ctx);

                        if (childPlan.HasWork)
                        {
                            currentPlan.ChildWalks.Add(new ChildWalk
                            {
                                Field = ancestorField,
                                PropertyClrType = ancestorPropType,
                                IsArray = member.MemberType == EnumMemberType.Array,
                                BindMethodEmitter = childPlan.BindRecursiveMethod
                            });
                        }

                        continue;
                    }
                    // @AI-Generated-End

                    int planCountBefore = ctx.AllPlans.Count;

                    Type nestedType = CreateNestedType(typeEmitter, member, level + 1, allMembers, typeCache, currentPlan, ctx);

                    enumMemberType = member.MemberType == EnumMemberType.Array
                        ? nestedType.MakeArrayType()
                        : nestedType;

                    if (member.MemberType is EnumMemberType.Object or EnumMemberType.Array
                        && ctx.AllPlans.Count > planCountBefore)
                    {
                        ScopePlan addedPlan = ctx.AllPlans[planCountBefore];

                        childPlansByMember[member] = addedPlan;
                    }
                }
                else
                {
                    enumMemberType = GetTypeFromEnumMemberType(member.MemberType, member.Required);
                }

                typeCache[member.Code] = enumMemberType;

                // @AI-Generated by Copilot 2026-04-28 18:00:00
                (PropertyEmitter propEmitter, MethodEmitter getterEmitter, FieldEmitter fieldEmitter) = DefineProperty(typeEmitter, member, code,
                    enumMemberType, isCrossScopeMemberRef ? memberRefImmediateTarget : null);

                gettersByCode[member.Code] = getterEmitter;
                ctx.GettersByCode[member.Code] = getterEmitter;
                ctx.ScopeByMemberCode[member.Code] = currentPlan;
                ctx.TypesByMemberCode[member.Code] = enumMemberType;

                // 只为非 MemberRef 叶子记录字段，后续跨作用域 Ref 从叶子字段读源值。
                if (member.MemberType != EnumMemberType.MemberRef)
                {
                    ctx.FieldsByCode[member.Code] = fieldEmitter;
                }

                if (isCrossScopeMemberRef)
                {
                    if (!ctx.ScopeByMemberCode.TryGetValue(memberRefLeafTarget, out ScopePlan targetScope))
                    {
                        throw new InvalidOperationException(
                            $"MemberRef cross-scope target {memberRefLeafTarget} has not been generated yet, declared at {member.Code}.");
                    }

                    if (targetScope == currentPlan)
                    {
                        throw new InvalidOperationException(
                            $"MemberRef cross-scope target {memberRefImmediateTarget} resolved to current scope, declared at {member.Code}.");
                    }

                    // 跨作用域 MemberRef 仅允许指向祖先作用域中的成员；指向平级兄弟分支无法在运行时定位。
                    string refParent = GetParentCode(member.Code);
