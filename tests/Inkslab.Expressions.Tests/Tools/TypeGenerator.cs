        /// <summary>
        /// 判断 <paramref name="ancestorParent"/> 标识的作用域是否是 <paramref name="descendantParent"/> 的祖先（或自身）。
        /// 作用域以"父成员代码"标识，根作用域为空字符串。
        /// </summary>
        private static bool IsAncestorOrSelfScope(string ancestorParent, string descendantParent)
        {
            if (ancestorParent.Length == 0)
            {
                return true;
            }

            if (string.Equals(ancestorParent, descendantParent, StringComparison.Ordinal))
            {
                return true;
            }

            return descendantParent.Length > ancestorParent.Length
                && descendantParent.StartsWith(ancestorParent, StringComparison.Ordinal)
                && descendantParent[ancestorParent.Length] == '.';
        }
