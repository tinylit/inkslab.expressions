                            $"only ancestor or same-scope targets are supported.");
                    }

                    if (!ctx.FieldsByCode.TryGetValue(memberRefLeafTarget, out FieldEmitter targetField))
                    {
                        throw new InvalidOperationException(
                            $"MemberRef cross-scope target {memberRefLeafTarget} has no backing field; only non-MemberRef leaves are supported as cross-scope sources.");
